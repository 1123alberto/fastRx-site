/* ==========================================================================
   State Management & Translation Logic
   ========================================================================== */

let currentLang = 'gr';

/**
 * Update DOM elements with translations based on current language
 */
function updateDOM() {
  // Update document language tag
  document.documentElement.lang = currentLang;

  // Translate standard text nodes
  const translatableElements = document.querySelectorAll('[data-i18n]');
  translatableElements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (COPY[currentLang] && COPY[currentLang][key]) {
      element.innerHTML = COPY[currentLang][key];
    }
  });

  document.querySelectorAll('[data-i18n-aria]').forEach(element => {
    const key = element.getAttribute('data-i18n-aria');
    if (COPY[currentLang]?.[key]) element.setAttribute('aria-label', COPY[currentLang][key]);
  });
  const languageInput = document.getElementById('form-language');
  if (languageInput) languageInput.value = currentLang;

  // Translate document metadata/title tags
  const titleElement = document.querySelector('title');
  if (titleElement && COPY[currentLang]['meta-title']) {
    titleElement.textContent = COPY[currentLang]['meta-title'];
  }

  const metaElements = document.querySelectorAll('[data-i18n-meta]');
  metaElements.forEach(element => {
    const attrName = element.getAttribute('data-i18n-meta');
    const key = attrName === 'description' ? 'meta-description' : attrName;
    if (COPY[currentLang] && COPY[currentLang][key]) {
      element.setAttribute('content', COPY[currentLang][key]);
    }
  });

  // Update header toggle button visual states
  const labelGR = document.getElementById('label-gr');
  const labelEN = document.getElementById('label-en');
  if (labelGR && labelEN) {
    if (currentLang === 'gr') {
      labelGR.classList.add('active');
      labelEN.classList.remove('active');
    } else {
      labelGR.classList.remove('active');
      labelEN.classList.add('active');
    }
  }

  // Handle unique status badge padding tweak based on language if needed
  const badge = document.querySelector('.status-badge');
  if (badge) {
    badge.style.letterSpacing = currentLang === 'gr' ? '0.02em' : '0.05em';
  }
}

/**
 * Change the active language state
 * @param {string} lang - 'gr' or 'en'
 */
function setLanguage(lang) {
  if (lang === 'gr' || lang === 'en') {
    currentLang = lang;
    localStorage.setItem('fastrx_lang', lang);
    updateDOM();
  }
}

/**
 * Restore an explicit saved preference; otherwise default to Greek.
 */
function initLanguage() {
  const savedLang = localStorage.getItem('fastrx_lang');
  if (savedLang && (savedLang === 'gr' || savedLang === 'en')) {
    currentLang = savedLang;
  } else {
    currentLang = 'gr';
  }
  updateDOM();
}

/* ==========================================================================
   Interaction & Event Handlers
   ========================================================================== */

document.addEventListener('DOMContentLoaded', () => {
  // Initialize translations
  initLanguage();

  // 1. Language Toggle Button Event
  const langBtn = document.getElementById('lang-btn');
  if (langBtn) {
    langBtn.addEventListener('click', () => {
      const nextLang = currentLang === 'gr' ? 'en' : 'gr';
      setLanguage(nextLang);
    });
  }

  // 2. Contact form
  const contactForm = document.getElementById('contact-form');
  const submitButton = document.getElementById('contact-submit');
  const formStatus = document.getElementById('form-status');
  let submitting = false;

  const clearErrors = () => {
    contactForm?.querySelectorAll('[data-error-for]').forEach(error => { error.textContent = ''; });
    contactForm?.querySelectorAll('[aria-invalid="true"]').forEach(field => field.removeAttribute('aria-invalid'));
  };

  const validateForm = () => {
    clearErrors();
    const data = new FormData(contactForm);
    const errors = {};
    for (const field of ['name', 'email', 'specialty', 'reason', 'message']) {
      if (!String(data.get(field) || '').trim()) errors[field] = COPY[currentLang]['validation-required'];
    }
    const email = String(data.get('email') || '').trim();
    if (email && !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email)) errors.email = COPY[currentLang]['validation-email'];
    const messageLength = String(data.get('message') || '').trim().length;
    if (messageLength && (messageLength < 20 || messageLength > 4000)) errors.message = COPY[currentLang]['validation-message'];

    Object.entries(errors).forEach(([name, message]) => {
      const field = contactForm.elements.namedItem(name);
      const error = contactForm.querySelector(`[data-error-for="${name}"]`);
      field?.setAttribute('aria-invalid', 'true');
      if (error) error.textContent = message;
    });
    if (Object.keys(errors).length) contactForm.elements.namedItem(Object.keys(errors)[0])?.focus();
    return Object.keys(errors).length === 0;
  };

  if (contactForm && submitButton && formStatus) {
    contactForm.addEventListener('submit', async event => {
      event.preventDefault();
      if (submitting || !validateForm()) return;
      submitting = true;
      submitButton.disabled = true;
      submitButton.textContent = COPY[currentLang]['form-sending'];
      formStatus.textContent = '';
      formStatus.className = 'form-status form-field-wide';

      try {
        const payload = Object.fromEntries(new FormData(contactForm).entries());
        const response = await fetch(contactForm.action, {
          method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(payload)
        });
        if (!response.ok) throw new Error('Delivery failed');
        contactForm.reset();
        document.getElementById('form-language').value = currentLang;
        clearErrors();
        formStatus.textContent = COPY[currentLang]['form-success'];
        formStatus.classList.add('is-success');
      } catch {
        formStatus.textContent = COPY[currentLang]['form-error'];
        formStatus.classList.add('is-error');
      } finally {
        submitting = false;
        submitButton.disabled = false;
        submitButton.textContent = COPY[currentLang]['form-submit'];
        formStatus.focus();
      }
    });
  }

  // 3. Privacy Policy Modal Controls
  const privacyTrigger = document.getElementById('privacy-trigger');
  const privacyModal = document.getElementById('privacy-modal');
  const modalCloseBtn = document.getElementById('modal-close-btn');
  const modalConfirmBtn = document.getElementById('modal-confirm-btn');

  function openModal() {
    if (privacyModal) {
      privacyModal.classList.add('open');
      privacyModal.setAttribute('aria-hidden', 'false');
      document.body.style.overflow = 'hidden'; // Lock background scrolling
    }
  }

  function closeModal() {
    if (privacyModal) {
      privacyModal.classList.remove('open');
      privacyModal.setAttribute('aria-hidden', 'true');
      document.body.style.overflow = ''; // Unlock background scrolling
    }
  }

  if (privacyTrigger) {
    privacyTrigger.addEventListener('click', openModal);
  }

  if (modalCloseBtn) {
    modalCloseBtn.addEventListener('click', closeModal);
  }

  if (modalConfirmBtn) {
    modalConfirmBtn.addEventListener('click', closeModal);
  }

  // Close modal when clicking outside content area
  if (privacyModal) {
    privacyModal.addEventListener('click', (e) => {
      if (e.target === privacyModal) {
        closeModal();
      }
    });

    // Close on Escape key press
    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape' && privacyModal.classList.contains('open')) {
        closeModal();
      }
    });
  }
});
