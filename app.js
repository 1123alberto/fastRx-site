/* ==========================================================================
   State Management & Translation Logic
   ========================================================================== */

let currentLang = 'gr';

/**
 * Update DOM elements with translations based on current language
 */
function updateDOM() {
  // Update document language tag
  document.documentElement.lang = currentLang;

  // Translate standard text nodes
  const translatableElements = document.querySelectorAll('[data-i18n]');
  translatableElements.forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (COPY[currentLang] && COPY[currentLang][key]) {
      element.innerHTML = COPY[currentLang][key];
    }
  });

  // Translate document metadata/title tags
  const titleElement = document.querySelector('title');
  if (titleElement && COPY[currentLang]['meta-title']) {
    titleElement.textContent = COPY[currentLang]['meta-title'];
  }

  const metaElements = document.querySelectorAll('[data-i18n-meta]');
  metaElements.forEach(element => {
    const attrName = element.getAttribute('data-i18n-meta');
    const key = attrName === 'description' ? 'meta-description' : attrName;
    if (COPY[currentLang] && COPY[currentLang][key]) {
      element.setAttribute('content', COPY[currentLang][key]);
    }
  });

  // Update header toggle button visual states
  const labelGR = document.getElementById('label-gr');
  const labelEN = document.getElementById('label-en');
  if (labelGR && labelEN) {
    if (currentLang === 'gr') {
      labelGR.classList.add('active');
      labelEN.classList.remove('active');
    } else {
      labelGR.classList.remove('active');
      labelEN.classList.add('active');
    }
  }

  // Handle unique status badge padding tweak based on language if needed
  const badge = document.querySelector('.status-badge');
  if (badge) {
    badge.style.letterSpacing = currentLang === 'gr' ? '0.02em' : '0.05em';
  }
}

/**
 * Change the active language state
 * @param {string} lang - 'gr' or 'en'
 */
function setLanguage(lang) {
  if (lang === 'gr' || lang === 'en') {
    currentLang = lang;
    localStorage.setItem('fastrx_lang', lang);
    updateDOM();
  }
}

/**
 * Detect default language from localstorage, browser settings, or system settings
 */
function initLanguage() {
  const savedLang = localStorage.getItem('fastrx_lang');
  if (savedLang && (savedLang === 'gr' || savedLang === 'en')) {
    currentLang = savedLang;
  } else {
    // Detect Greek from browser languages
    const browserLanguages = navigator.languages || [navigator.language || navigator.userLanguage];
    const prefersGreek = browserLanguages.some(l => l.toLowerCase().startsWith('el'));
    currentLang = prefersGreek ? 'gr' : 'en';
  }
  updateDOM();
}

/* ==========================================================================
   Interaction & Event Handlers
   ========================================================================== */

document.addEventListener('DOMContentLoaded', () => {
  // Initialize translations
  initLanguage();

  // 1. Language Toggle Button Event
  const langBtn = document.getElementById('lang-btn');
  if (langBtn) {
    langBtn.addEventListener('click', () => {
      const nextLang = currentLang === 'gr' ? 'en' : 'gr';
      setLanguage(nextLang);
    });
  }

  // 2. Email Copy-to-Clipboard Handler
  const copyBtn = document.getElementById('copy-email-btn');
  const copyFeedback = document.getElementById('copy-feedback');
  const emailText = 'info@fastrx.gr';

  if (copyBtn && copyFeedback) {
    copyBtn.addEventListener('click', () => {
      navigator.clipboard.writeText(emailText)
        .then(() => {
          // Success Feedback
          copyFeedback.textContent = COPY[currentLang]['copy-btn-success'];
          copyBtn.classList.add('btn-primary');
          copyBtn.classList.remove('btn-secondary');

          // Reset button after 2 seconds
          setTimeout(() => {
            copyFeedback.textContent = COPY[currentLang]['copy-btn-text'];
            copyBtn.classList.remove('btn-primary');
            copyBtn.classList.add('btn-secondary');
          }, 2000);
        })
        .catch(err => {
          console.error('Failed to copy email to clipboard: ', err);
        });
    });
  }

  // 3. Privacy Policy Modal Controls
  const privacyTrigger = document.getElementById('privacy-trigger');
  const privacyModal = document.getElementById('privacy-modal');
  const modalCloseBtn = document.getElementById('modal-close-btn');
  const modalConfirmBtn = document.getElementById('modal-confirm-btn');

  function openModal() {
    if (privacyModal) {
      privacyModal.classList.add('open');
      privacyModal.setAttribute('aria-hidden', 'false');
      document.body.style.overflow = 'hidden'; // Lock background scrolling
    }
  }

  function closeModal() {
    if (privacyModal) {
      privacyModal.classList.remove('open');
      privacyModal.setAttribute('aria-hidden', 'true');
      document.body.style.overflow = ''; // Unlock background scrolling
    }
  }

  if (privacyTrigger) {
    privacyTrigger.addEventListener('click', openModal);
  }

  if (modalCloseBtn) {
    modalCloseBtn.addEventListener('click', closeModal);
  }

  if (modalConfirmBtn) {
    modalConfirmBtn.addEventListener('click', closeModal);
  }

  // Close modal when clicking outside content area
  if (privacyModal) {
    privacyModal.addEventListener('click', (e) => {
      if (e.target === privacyModal) {
        closeModal();
      }
    });

    // Close on Escape key press
    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape' && privacyModal.classList.contains('open')) {
        closeModal();
      }
    });
  }
});
